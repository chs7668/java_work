package ex2_break;

public class Ex1_break {
	public static void main(String[] args) {
		//break문 : 반복문 내에서 break문을만나게 되면 가장 가까운 반복문을 빠져나갈때 사용
		
		for(int i=1; i<=2; i++) {
			for(int j=1; j<=5; j++) {
				if(j%2==0) {
					break;
				}
					
				System.out.printf("%d ",j);
			}
			System.out.println();
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
