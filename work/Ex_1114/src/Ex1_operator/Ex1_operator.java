package Ex1_operator;

public class Ex1_operator {

	public static void main(String[] args) {
		//연산자
		/*
		 * 1. 최고연산자:.()
		 * 2. 증감연산자: ++,--
		 * 3. 산술연산자: +,-,*,/,%(모듈러)
		 * 4. 시프트연산자: >>,<<,>>>
		 * 5. 비교연산자: <,>,<=,>=,==,!=
		 * 6. 비트연산자: &,|,^,~
		 * 7. 논리연산자 &&, ||, !
		 * 8. 조건(삼항)연산자: ?, :
		 * 9. 대입연산자 : =, +=, -=, *=, /=, %=
		 */
		
		int n1,n2,n3;
		
		n1 = 20;
		n2 = 7;
		n3 = n1+n2;
		System.out.println("n1+n2="+n3);
		
		n3 = n1-n2;
		System.out.println("n1-n2="+n3);

		n3 = n1*n2;
		System.out.println("n1*n2="+n3);
		
		n3 = n1/n2;
		System.out.println("n1/n2="+n3);
		
		n3 = n1%n2;
		System.out.println("n1%n2="+n3);
		// %(모듈러)를 많이 사용하는것
		//-짝수홀수 가릴떼(2로 나누면 알수있음)
		//-N의 배수 구할때 
		
		
		
		
		
	}

}
